public class Circle {
    //Exercise 1.8
    public static void main(String[] args) {
        double pi = 3.14159;
        double radious = 6.5;
        double perimeter = 2 * radious * pi;
        double area = radious * radious * pi;
        System.out.println("Perimeter: " + perimeter);
        System.out.println("Area:      " + area);
    }
}
